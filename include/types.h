#ifndef SHADOW_NATIVE_TYPES_H
#define SHADOW_NATIVE_TYPES_H

#include <cinttypes>

typedef uint8_t u8;
typedef uint16_t u16;
typedef uint32_t u32;
typedef uint64_t u64;

typedef int8_t i8;
typedef int16_t i16;
typedef int32_t i32;
typedef int64_t i64;

typedef std::size_t usize;
typedef __ssize_t isize;

typedef float f32;
typedef double f64;

#endif //SHADOW_NATIVE_TYPES_H
